let currentIndex = 0;
let score = 0;
const music = document.getElementById('bg-music');
let isPlaying = false;

function checkLogin() {
    const pass = document.getElementById('pass-input').value;
    if (pass === '7777') {
        document.getElementById('login-screen').classList.add('hidden');
        document.getElementById('home-screen').classList.remove('hidden');
        music.play().then(() => { isPlaying = true; }).catch(e => console.log("Музыканы қосу үшін экранды басыңыз"));
    } else {
        document.getElementById('error-msg').innerText = "Пароль қате!";
    }
}

function toggleMusic() {
    if (isPlaying) {
        music.pause();
        document.getElementById('music-control').innerText = '🔇';
    } else {
        music.play();
        document.getElementById('music-control').innerText = '🎵';
    }
    isPlaying = !isPlaying;
}

function startQuiz() {
    document.getElementById('home-screen').classList.add('hidden');
    document.getElementById('quiz-screen').classList.remove('hidden');
    renderQuestion();
}

function renderQuestion() {
    if (currentIndex >= questions.length) return showResult();

    const q = questions[currentIndex];
    document.getElementById('q-counter').innerText = `${currentIndex + 1} / ${questions.length}`;
    document.getElementById('question-display').innerText = q.q;
    document.getElementById('progress-fill').style.width = `${((currentIndex + 1) / questions.length) * 100}%`;

    const box = document.getElementById('options-box');
    box.innerHTML = '';

    q.a.forEach((ans, i) => {
        const btn = document.createElement('button');
        btn.className = 'option-btn';
        btn.innerText = ans;
        btn.onclick = () => {
            const btns = document.querySelectorAll('.option-btn');
            btns.forEach(b => b.disabled = true);

            if (i === q.c) {
                btn.style.background = '#22c55e';
                btn.style.color = 'white';
                score++;
            } else {
                btn.style.background = '#ef4444';
                btn.style.color = 'white';
                btns[q.c].style.background = '#22c55e';
                btns[q.c].style.color = 'white';
            }

            setTimeout(() => {
                currentIndex++;
                renderQuestion();
            }, 1000);
        };
        box.appendChild(btn);
    });
}

function showResult() {
    document.getElementById('quiz-screen').classList.add('hidden');
    document.getElementById('result-screen').classList.remove('hidden');
    const percent = Math.round((score / questions.length) * 100);
    document.getElementById('final-result').innerHTML = `
        <h2>Нәтиже: ${percent}%</h2>
        <p>Дұрыс жауаптар: ${score} / ${questions.length}</p>
    `;
}
